import { Link } from "react-router-dom"
import { Input } from "@/components/ui/input"
import { DropdownMenu, DropdownMenuTrigger, DropdownMenuContent, DropdownMenuLabel, DropdownMenuSeparator, DropdownMenuItem } from "@/components/ui/dropdown-menu"
import { Button } from "@/components/ui/button"
import { Badge } from "@/components/ui/badge"
import { useState } from "react"
import axios from "axios";

export default function Component() {
  const [isModalOpen, setIsModalOpen] = useState(false);
  const [file, setFile] = useState(null);

  const handleFileUpload = (e) => {
    setFile(e.target.files[0]);
    setIsModalOpen(true);
  };

  const handleFileUploadSubmit = async () => {
    if (!file) return;

    const formData = new FormData();
    formData.append("file", file);

    try {
      const response = await axios.post("https://your-api-endpoint.com/upload", formData, {
        headers: {
          "Content-Type": "multipart/form-data",
        },
      });
      console.log("File uploaded successfully:", response.data);
    } catch (error) {
      console.error("Error uploading file:", error);
    }

    setIsModalOpen(false);
  };

  const handleCloseModal = () => {
    setIsModalOpen(false);
    setFile(null);
  };

  return (
    <header className="flex h-14 lg:h-[60px] items-center gap-4 border-b bg-muted/40 px-6">
      <Link to="#" className="lg:hidden" href="#" prefetch={false}>
        <Package2Icon className="h-6 w-6" />
        <span className="sr-only">Home</span>
      </Link>
      <div className="flex-1">
        <h1 className="font-semibold text-lg">Contracts</h1>
      </div>
      <div className="flex flex-1 items-center gap-4 md:ml-auto md:gap-2 lg:gap-4">
        <form className="ml-auto flex-1 sm:flex-initial">
          <div className="relative">
            <SearchIcon className="absolute left-2.5 top-2.5 h-4 w-4 text-muted-foreground" />
            <Input
              type="search"
              placeholder="Search contracts..."
              className="pl-8 sm:w-[300px] md:w-[200px] lg:w-[300px]"
            />
          </div>
        </form>
        <div className="relative">
          <Button variant="outline" size="icon" className="rounded-full" onClick={() => setIsModalOpen(true)}>
            <UploadIcon className="h-5 w-5" />
            <span className="sr-only">Upload file</span>
          </Button>
          {isModalOpen && (
            <div className="absolute right-0 z-10 mt-2 w-80 rounded-md bg-background p-4 shadow-lg">
              <div className="flex justify-between items-center">
                <h3 className="text-lg font-medium">Upload File</h3>
                <Button variant="ghost" size="icon" onClick={handleCloseModal}>
                  <XIcon className="h-5 w-5" />
                  <span className="sr-only">Close</span>
                </Button>
              </div>
              <div className="mt-4">
                <label htmlFor="file-upload" className="block cursor-pointer text-sm font-medium text-primary">
                  Choose a file
                </label>
                <input id="file-upload" type="file" className="sr-only" onChange={handleFileUpload} />
                {file && (
                  <div className="mt-2 flex items-center justify-between">
                    <span className="text-sm">{file.name}</span>
                    <Button variant="outline" size="xs" onClick={handleFileUploadSubmit}>
                      Upload
                    </Button>
                  </div>
                )}
              </div>
            </div>
          )}
        </div>
        <DropdownMenu>
          <DropdownMenuTrigger asChild>
            <Button variant="ghost" size="icon" className="rounded-full">
              <UserIcon className="h-5 w-5 ml-2" />
              <span className="sr-only">Toggle user menu</span>
            </Button>
          </DropdownMenuTrigger>
          <DropdownMenuContent align="end">
            <DropdownMenuLabel>My Account</DropdownMenuLabel>
            <DropdownMenuSeparator />
            <DropdownMenuItem>Settings</DropdownMenuItem>
            <DropdownMenuItem>Support</DropdownMenuItem>
            <DropdownMenuSeparator />
            <DropdownMenuItem>Logout</DropdownMenuItem>
            <DropdownMenuSeparator />
            <div className="flex items-center justify-between px-4 py-2 text-sm font-medium">
              <span>Compliance Status</span>
              <div className="flex items-center gap-2">
                <Badge variant="outline" className="bg-red-100 text-red-600">
                  Breached
                </Badge>
                <Badge variant="outline" className="bg-green-100 text-green-600">
                  Compliant
                </Badge>
                <Badge variant="outline" className="bg-gray-100 text-gray-600">
                  Unknown
                </Badge>
              </div>
            </div>
          </DropdownMenuContent>
        </DropdownMenu>
      </div>
    </header>
  );
}

